package C8Loops;

public class WhileLoopDemom3 {
    public static void main(String[] args) {


        int i=10;
        while (i>0){
            System.out.println(i);
            i--;
        }
        int k=10;
        while (k>0){
            if(k<5){
                System.out.println(k);
            }
            k--;
        }
    }
}
