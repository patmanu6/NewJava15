package C8Loops;

public class MixExamples {
    public static void main(String[] args) {
         /*
        Write a loop to print below numbers solve below tasks with while loop and
for loop
1) 2 3 4 5 6
         */
        for (int i = 2; i <=6 ; i++) {
            System.out.print(i+" ");
        }

        System.out.println();
        int num=2;
        while (num<=6){
            System.out.print(num+" ");
            num++;
        }
    }
}
