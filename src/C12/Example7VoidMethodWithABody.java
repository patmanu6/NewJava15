package C12;

public class Example7VoidMethodWithABody {

    /*
    void => is a return type
    printHelloWorld=> name of the method
    (int number)=> parameters or input to the method
    {}=> body of the method
     */
    void printHelloWorld(int number) {
        for (int i = 0; i < number; i++) {
            System.out.println("Hello World");
        }
    }
}
