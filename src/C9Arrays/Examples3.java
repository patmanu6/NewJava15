package C9Arrays;

public class Examples3 {
    public static void main(String[] args) {

        int [] numbers={10,20,45,67};
        // print all the elements from this array with the help of a
        // while loop
        int num=0;
        while (num<numbers.length){
            System.out.println(numbers[num]);
            num++;
        }


    }
}
